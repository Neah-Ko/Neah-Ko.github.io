<h1>Automatic Biathlon targets</h1>
<p class='main_para'>
	This project was my end of studies' Engineering project at High school of Briançon. We had to modify an existing biathlon target in order to make it automatic.<br>
	In a competition context, jury should be able to see state of the target (targets lowered)
	 and perform actions on it (reset targets) from control station thanks to an android application.<br>
	I was given programmation part : I used a Pickaxe microprocessor to handle sensors and bluetooth inputs and generate bluetooth frames.
	Then i created the android app with <a href="http://ai2.appinventor.mit.edu/" target="_blank">MIT App Inventor</a> a framework made by MIT.<br><br>
	In the end we had a fully functional application that could display several targets at once and send orders on them.<br><img id="tsi_app" src="images/projects/content/tsi_app.jpg">
</p>
