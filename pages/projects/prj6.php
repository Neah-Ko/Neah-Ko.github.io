<h1>Project NINA</h1>
<p class='main_para'>
This project is my end of studies' project at IUT Annecy-le-Vieux. We're currently working on it in a team of 4. It should last unti April 2017<br>
It consists in create an Artificial intelligence that can browse on the web automatically without being detectable in order to generate noise on our virtual identity to defeat Add Trackers.<br><br>
It's currently developped for Debian Testing(4.7) using C, Bourne against shell, and python. We use a specific python framework : scrappy that we need for scrapping web content.

</p>
