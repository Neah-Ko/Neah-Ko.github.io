<h1>TripAdvisor Scrum</h1>
<p class='main_para'>
	This project was a simulation of an Agile Scrum project organized by our teachers.<br>
	We had to reproduce functionalities of the website <a href="https://www.tripadvisor.co.uk/" target="_blank">TripAdvisor</a> under Specifications constraints, among them an imposed database script<br><br>
	The website uses HTML, Object oriented PHP, JavaScripts, PostgreSql and we handled our versioning with github.<br><br>
	We implemented all main functionalities of the original website such as inscription, connection, Reseach engine, and so on.<br>
	For this project we tried to do as much automatisation as possible in PHP code, with a generic model extended by classes.
</p>
